@section('requiredjs')
    <script>
        new Vue({
            el: '#home',
            data() {
                return {
                    sendNowEnable: true,
                    alert: false,
                    alertStyle: '',
                    alertMessage: '',
                    form: {
                        name: '',
                        email: '',
                        phone: '',
                        address: '',
                    }
                }
            },
            methods: {
                resetContactForm() {
                    this.form = {};
                },
                clearAlert() {
                    this.alert = false;
                    this.alertStyle = '';
                    this.alertMessage = '';
                },
                requestJoinUs() {
                    this.sendNowEnable = false;
                    axios.post('send-join-us-mail', this.form, {
                        headers: {
                            'content-type': 'application/json'
                        }
                    })
                        .then(res => {
                            this.sendNowEnable = true;
                            if (res.data.status) {
                                this.alert = true;
                                this.alertStyle = 'color:green';
                                this.alertMessage = res.data.message;
                                this.resetContactForm()

                            } else {
                                this.alert = true;
                                this.alertStyle = 'color:red';
                                this.alertMessage = res.data.error;
                            }
                        });
                    setInterval(() => this.clearAlert(), 10000);
                }
            },
        })
    </script>
